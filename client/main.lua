function OpenCloakroomMenu()

  local elements = {
    {label = _U('siviili_vaate'), value = 'citizen_wear'},
    {label = _U('jengi_vaate'), value = 'mafia_wear'}
  }

  ESX.UI.Menu.CloseAll()

	ESX.UI.Menu.Open(
		'default', GetCurrentResourceName(), 'cloakroom',
		{
			title    = _U('vaatehuone'),
			elements = {
				{label = _U('siviili_vaate'), value = 'mafia_wear'},
				{label = _U('jengi_vaate'), value = 'citizen_wear'}
